    void CentrarVentana(JInternalFrame frame) {
        panelEscritorio.add(frame);
        Dimension dim = panelEscritorio.getSize();
        Dimension framesise = frame.getSize();
        frame.setLocation((dim.width - framesise.width) / 2, (dim.height - framesise.height) / 2);
        frame.show();
    }
    
    private void calculos()
    {
        int cantidadAcumulada=0;
        int cantidadDebito=0;
        int cantidadTranf=0;
        int cantidadTarjeta=0;
        int cantidadEfectivo=0;
        double porcTarjeta=0.0;
        double porcEfectivo=0.0;
        double porcDebito=0.0;
        double porcTrans=0.0;
        double monto =0;
        for (int i = 0; i < tablaVentas.getRowCount(); i++) 
        {
            cantidadAcumulada += Integer.valueOf(tablaVentas.getValueAt(i, 6).toString());
            monto += Double.valueOf(tablaVentas.getValueAt(i, 7).toString().replace("$", "").trim());
            if(tablaVentas.getValueAt(i, 3).toString().contains("Tarjeta"))
            {
                cantidadTarjeta++;
            }
            if(tablaVentas.getValueAt(i, 3).toString().contains("Efectivo"))
            {
                cantidadEfectivo++;
            }
            if(tablaVentas.getValueAt(i, 3).toString().contains("Transferencia"))
            {
                cantidadTranf++;
            }
            if(tablaVentas.getValueAt(i, 3).toString().contains("Débito"))
            {
                cantidadDebito++;
            }
        }
        //5*100/10  parcial*100/parcial
        DecimalFormat f = new DecimalFormat("###,###.##");
        porcTarjeta = cantidadTarjeta * 100 / tablaVentas.getRowCount();
        porcEfectivo = cantidadEfectivo * 100 / tablaVentas.getRowCount();
        porcDebito = cantidadDebito * 100 / tablaVentas.getRowCount();
        porcTrans = cantidadTranf * 100 / tablaVentas.getRowCount();
        lblCantArtVendidos.setText(String.valueOf(cantidadAcumulada));
        lblMonto.setText("$"+String.valueOf(f.format(monto)));
        lblPorcTarjeta.setText(String.valueOf(porcTarjeta)+"%");
        lblPorcEfectivo.setText(String.valueOf(porcEfectivo)+"%");
        lblPorcDebito.setText(String.valueOf(porcDebito)+"%");
        lblPorcTrans.setText(String.valueOf(porcTrans)+"%");
    }
    
    private void cargarTabla() {

        try {

            ArrayList<VentaDTO> listado = new ArrayList();
            
            DefaultTableModel modelo = new DefaultTableModel();
            listado = g.getVentasFilroFechas(fechaInicio,fechaFinal);
            modelo.setColumnIdentifiers(new String[]{"Nro venta","Fecha","Hora","Forma de pago","Vendedor","Cliente Nro","Cant. art. vendida","Monto"});
            for (VentaDTO vta : listado) {
                Vector v = new Vector();
                v.add(vta.getNroVenta());
                v.add(vta.getFecha());
                v.add(vta.getHora());
                v.add(vta.getFormaPago());
                v.add(vta.getOperador());
                v.add(vta.getDni());
                v.add(vta.getCantVendida());
                v.add("$"+vta.getMontoTotal());
                modelo.addRow(v);
            }

            tablaVentas.setModel(modelo);
            //color de los bordes de las celdas
            int c =tablaVentas.getRowCount();
            lblCantidadFilas.setText(String.valueOf(c));
            tablaVentas.setGridColor(new java.awt.Color(214, 213, 208));
            //tamaño de columnas
            
            tablaVentas.getColumnModel().getColumn(0).setPreferredWidth(60);
            tablaVentas.getColumnModel().getColumn(4).setPreferredWidth(90);
            tablaVentas.getColumnModel().getColumn(0).setPreferredWidth(60);
            //altura de filas
            tablaVentas.setRowHeight(24);
            //se asigna el nuevo CellRenderer a cada columna segun su contenido
            tablaVentas.getColumnModel().getColumn(0).setCellRenderer(new CellRenderer("minimo"));
            tablaVentas.getColumnModel().getColumn(5).setCellRenderer(new CellRenderer("minimo"));
            tablaVentas.getColumnModel().getColumn(2).setCellRenderer(new CellRenderer("hour"));
            tablaVentas.getColumnModel().getColumn(3).setCellRenderer(new CellRenderer("text"));
            tablaVentas.getColumnModel().getColumn(4).setCellRenderer(new CellRenderer("text"));
            tablaVentas.getColumnModel().getColumn(6).setCellRenderer(new CellRenderer("num"));
            tablaVentas.getColumnModel().getColumn(7).setCellRenderer(new CellRenderer("moneda"));
            //Se asigna nuevo header a la tabla
            JTableHeader jtableHeader = tablaVentas.getTableHeader();
            jtableHeader.setDefaultRenderer(new HeaderCellRenderer());
            tablaVentas.setTableHeader(jtableHeader);
           
        } catch (Exception e) {

        }
    }
    
    

